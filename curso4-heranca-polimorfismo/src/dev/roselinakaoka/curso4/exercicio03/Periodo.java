package dev.roselinakaoka.curso4.exercicio03;

/*
 * Enumeração Periodo
 *
 * Representa os dois períodos do dia utilizados em relógios no formato 12 horas:
 * - AM (Ante Meridiem): antes do meio-dia (00h às 11h59)
 * - PM (Post Meridiem): após o meio-dia (12h às 23h59)
 *
 * Esta enum é usada na classe USClock para indicar se a hora é AM ou PM.
 *
 * Autor: Roseli Nakaoka
 * Data: 26/07/2025
 */

public enum Periodo {
    AM,
    PM
}
